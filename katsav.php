<h2>Modeling the argument</h2>
<div class="opening">
<ul>
<li><u>Who VS who</u> - The three complainants (A., H. and L.) against Moshe Katsav.</li>
<li><u>Goal</u> - To convict Katsav of significant offenses with actual imprisonment.</li>
<li>Thanks to our article, we can now present the argument using a network of signs.</li>
<li>S - The participants in the case.</li>
<li>R - Who VS who, that is, the accusers against the accused.</li>
<li>M - Life, the more life a participant has, the harder it is to "kill" him.</li>
<li>K - The power of the attack.</li>
<li>Î²<sub>m</sub> - A bonus of 1 attack power, when there are more than m accusers, we assign m=1 (we will get to it later on).</li>
