<h2>Sentence: On 30 December 2010</h2>
<img src="/images/katsav/demo2.png" title="Demonstration 2" alt="Demonstration 2">
<h4>We ask the question, why Katsav has 10 life if before he had 1?</h4>
<h2>Explanation:</h2>
<div class="opening">
<ul>
<li>Each sentence or plea bargain is essentially a rearrangement of all the factors. Therefore, we will use a new network in which Katsav's life remains 10. Moreover, more and more new supporting evidence has been added as well as the accuser H.</li>
<li>In 2010, Katsav was convicted by the Tel Aviv court of two rape offenses and an indecent assault of A. from the Tourism Ministry, A sexual harassment of A. from the President's Residence, For offenses of indecent assault and sexual harassment of L. from the President's Residence and for the offense of obstructing court proceedings when he tried to coordinate versions with L. from the President's Residence.</li>
<li>Would Katsav be in prison only from A.'s indictment? The indictment of two cases of rape and the commission of an indecent act is a very serious charge, but we speak of former President Moshe Katsav and there was no such precedent before in the State of Israel. It is doubtful that only A's attack on Katsav would be enough. For example, people would ask themselves - why would the former head of the council, a minister in the Israeli government and president of the state innocent so far, decide to rape Mrs. A.? From here we define A's attack on Katsav as 9, a tiny amount less of Katsav's life.</li>
<li>H. accused Katsav of sexual harassment. A slight offense relative to the rest. Let's define the power of the attack at 3.</li>
<li>L. accuses Katsav of committing an indecent act, sexual harassment and obstruction of justice. Although the offenses are difficult but relative to the indictment of A. These are lighter offenses but their power(H --> Katsav) is greater than the charge of H., Therefore: We define the attack power of L. as 5, between the strength of A. and H' attack.</li>
<li>Since there are similar charges from 3 complainants, we assume that the total attack strength will be slightly greater so we add another 1 to the total attack strength:  9 + 3 + 5 + 1 = 18 > the life of Katsav. Therefore, the intensity of the attack = 10.</li>
</ul>
