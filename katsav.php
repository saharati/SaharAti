<h2>A. from the Ministry of Tourism</h2>
<div class="opening">
<ul>
<li>The first charge relates to the work period of Katsav as Minister of Tourism and to Complainant A., who served in a senior position in his office during the relevant period.</li>
<li>In this charge, A. accuses Katsav of committing a rape offense on two occasions: one in Katsav's office in Tel Aviv in April 1998, and the second at the Plaza Hotel in Jerusalem on June 17, 1998.</li>
<li>In addition, Katsav is charged with an offense of indecent assault which was committed in the apartment of A.</li>
</ul>
</div>
<div class="closing">
<h4>Supporting evidence</h4>
<p>
At first, Katsav complained of an attempt to blackmail him by A., due to her threats of filing a sexual harassment complaint, however, the testimonies of A.'s statements regarding the violation of her by Katsav in real time, deny the claims raised by Katsav.<br>
In this context we note the testimony of Shaul Halali, who spoke with Katsav, at the request of A., before her dismissal, in order to try to improve the atmosphere that prevailed in the office.<br>
A. told Halali that she was "very upset" because Katsav "sends long hands".<br>
Not only did he meet Katsav in his office, but Katsav even phoned him the day after the meeting in order to ascertain his impression.<br>
When Katsav's response to his testimony was requested, he denied, with ostentatious contempt, his meeting with Halali, as well as his conversation with him, even though his defense counsel had to admit afterwards that the meeting was held, since it was mentioned in the letter of dismissal, Written by Katsav himself.<br>
We shall also mention the testimony of Shmuel Tzurel, who served at the relevant times in a senior position in the Ministry of Tourism, who told the court that A. told him that Katsav was sexually harassing her.<br>
When Tzurel appealed to the director-general of the ministry, even without mentioning the content of A.'s complaint regarding the sexual harassment of Katsav, so that the latter would talk to A., he was reprimanded by Katsav who saw his interference with great severity.<br>
A special importance was attributed to the testimony of Attorney Ben-Tovim, to whom A. applied the day after her dismissal by Katsav, who told the court that A. told him: "The Minister harassed her sexually and that she felt that her work came to an end because she did not agree with him".<br>
Attorney Ben-Tovim confirmed that he had "blocked his ears" from hearing about the sexual issue, even though he understood that it was more than sexual harassment, but he wrote a copy of A.'s statement, which he kept in a safe and presented to the court at the testimony.<br>
Other statements by A. regarding her sexual harassment by Katsav long before the affair broke out were heard by Dror Nissan, Yaron Armoza, Dr. Michal Sela, Ilana Dayan and others.<br>
All of the above statements, which came from A.'s mouth at the time of the events, contradict Katsav's contention that this is a later invention in order to get revenge.
</p>
