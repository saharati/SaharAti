<h2>H. from the President's Residence</h2>
<div class="opening">
<ul>
<li>H. accuses Katsav of three frontal hugs in 2003 when he served as president aswell as sexual harassment while exploiting authority.</li>
<li>H. claimed all along in her statements to the police about the hugs. In her version, these are hugs of an intimate nature, in which Katsav pressed her against his chest. H. insisted that she told Katsav after the first two hugs that they were not in her favor and asked him to stop it, but Katsav continued and hugged her again for the third time. Additionally, her testimony was consistent with her statements to the police and the State Attorney's Office, and was noted for her minority, caution and precision, as well as the fact that she did not want to complain against Katsav, and was dragged into this case when she did not wish for it.</li>
</ul>
</div>
<div class="closing">
<h4>Supporting evidence</h4>
<ul>
<li>Further reinforcement of the existence of sexual significanced hugs were found, in testimonies of "similar acts" which came from the words of L., N.A. and N.R. who also had a similar experience of hug, while they worked for Katsav. These testimonies serve to reinforce Katsav's intentions and reject the following argument from his mouth about a random and innocent hug.</li>
<li>Various victims described a man who was close to Katsav, helping him to terrorize the victims and lead them into his arms.</li>
</ul>
