<h2>L. from the President's Residence</h2>
<div class="opening">
<ul>
<li>In the third charge we are dealing with the embraces that the defendant embraced L., who was 25 when she worked in the President's Residence and was subordinate to his authority, as well as statements of sexual content he had told her. In respect of these, the defendant is charged with offenses of indecent assault, as well as sexual harassment offenses while exploiting authority and labor relations.</li>
<li>The indictment alleges that during the working meetings held by the Defendant with L., while the two were in solitary confinement, the Defendant used to make personal remarks to her. So he told her that she is "cute" because she has "beautiful eyes" that she "looks beautiful today" because she is "dressed beautifully today" and so on.</li>
<li>The main point of the indictment relates to December 25, 2005, the date on which the defendant's 60th birthday party was held. L., who assisted in its organization, entered the defendant's office for the purpose of coordinating the final details. At the end of the meeting, she wished him a happy birthday. The defendant got up, told her that she deserved a hug, approached her and held her for a long time, pressing her body against his body, even tilting his face to her neck, to smell her, for sexual arousal. L., who was surprised by the defendant's act, left the office when she was upset and crying.</li>
</ul>
</div>
<div class="closing">
<h4>Supporting evidence</h4>
<ul>
<li>We can learn from N.R.'s, N.A.'s and H. testimonies of the defendant's practice of embracing the young women who are subject to him in unusual embraces. From N.A. We heard that he had hugged her on his birthday. After refusing to give him a kiss, he even asked her what she would do if he reached her room in the hotel, because of the question she canceled her participation in the conference she was about to take part in. The witness N.R. was also under the authority of the defendant, and told the court about two hugs that the defendant had embraced her. One hug by side, by the window and another, this time in front of her, when he returned from abroad. The defendant even called her in the evening and introduced himself as "Moshe" and offered to come visit her.</li>
<li>Various victims who did not know about each other described a pattern of repetitive harassment, whereby the initiation of "courtship" and an attempt to get close to the victims would begin in the offender's library and then continue in other places.</li>
</ul>
