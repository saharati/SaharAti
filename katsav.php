<h2>The plea bargain: June 28, 2007</h2>
<img src="/images/katsav/demo1.png" title="Demonstration 1" alt="Demonstration 1">
<h4>We ask the question, why 10 - 5 - 3 = 1?</h4>
<h2>Explanation:</h2>
<div class="opening">
<ul>
<li>Katsav, who was working as the president at that time was a very powerful person, so we shall give him a high amount of life: 10.</li>
<li>Back in 2007 due to problems in the schedule of A.'s testimony and difficulties in finding a proof required for proving rape, the prosecution decided it would be better to go for a plea bargain.</li>
<li>The plea bargain was very lenient for Katsav, in which he did not receive a prison sentence but only two years probation. Katsav was charged by A. only for indecent assault (see above) and by L. for sexual harassment.</li>
<li>It is seen from the deal that Katsav was not far from getting an active prison sentence, and that if Katsav had not been the president, he would have actually been arrested.</li>
<li>If we wanted to define our purpose in another way, for example only to convict him, we would place a lower value on the life of Katsav.</li>
<li>Due to the severity of A.'s accusation against Katsav and the allegations of two rapes that were not part of the plea bargain, we defined the power of A.'s attack against Katsav as 5.</li>
<li>In the plea bargain L. accused Katsav of sexual harassment, that was considered lesser offenses than indecent assaults and rape suspicions, so we defined the power of L.'s attack on Katsav about one-third of Katsav's life.</li>
<li>Since there are similar charges from 2 complainants we assume that the total attack strength will be slightly greater so we add 1 more to the overall attack strength: 5 + 3 + 1 = 9.</li>
</ul>
</div>
