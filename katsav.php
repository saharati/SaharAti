<h2>Conclusion:</h2>
<div class="opening">
<ul>
<li>The life of Katsav after the attack: Starting life (10) minus* [Addition of Î² (1) if there is more than one accuser (m = 1) + Kroncker Delta (1) multiplied by the attack power of L. on Katsav (5) + Kroncker Delta (1) multiplied by the attack power of H. on Katsav (3) + Kroncker Delta (1) multiplied by the attack power of A. on Katsav (9)] = 0. *What's inside the brackets must be less than or equal to the life of the attacked.</li>
<li>No one attacks the complainants, the complainants have already given their testimonies, so the delta of each one is 1.</li>
<li>From here we saw that Katsav was "dead", meaning that the charges were strong enough to put him in jail and indeed he received seven years in prison.</li>
<li>Epilogue: The judges noted that Katsav's alibi was refuted and his testimony was "laced with lies". As a result, he was the only president of the State of Israel convicted in the trial. On March 22, 2011, the District Court sentenced Katsav to seven years in prison, two years probation and 125,000 NIS refund for the complainants.</li>
