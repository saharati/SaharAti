<h2>My First Project</h2>
<p>
The way I got into computer science was special, at school I choose computer science but was about to quit due to bad marks.<br>
It was so different from what I imagined and it took me time to understand what's going on, because everything was done on paper.<br>
Only at a very late stage we had to do our first web project, I took the opportunity to build a website for an NGO I was volunteering at.<br>
Yet again I asked myself, what could a student with bad marks and only a few months of ASP and Access database experience do?<br>
